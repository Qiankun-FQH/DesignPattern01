public class DoFood {

    private String guo;

    private String cai;

    private String mi;

    private String chanhuodao;

    private String dao;

    public String getGuo() {
        return guo;
    }

    public void setGuo(String guo) {
        this.guo = guo;
    }

    public String getCai() {
        return cai;
    }

    public void setCai(String cai) {
        this.cai = cai;
    }

    public String getMi() {
        return mi;
    }

    public void setMi(String mi) {
        this.mi = mi;
    }

    public String getChanhuodao() {
        return chanhuodao;
    }

    public void setChanhuodao(String chanhuodao) {
        this.chanhuodao = chanhuodao;
    }

    public String getDao() {
        return dao;
    }

    public void setDao(String dao) {
        this.dao = dao;
    }
}
